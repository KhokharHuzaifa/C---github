// Numeric Types
// Use int when you need to store a whole number without decimals, like 35 or 1000, and float or double when you need 
// a floating point number (with decimals), like 9.99 or 3.14515

// int
int myNum = 1000;
cout << myNum;

// float
float myNum = 5.75;
cout << myNum;

// double
double myNum = 19.99;
cout << myNum;